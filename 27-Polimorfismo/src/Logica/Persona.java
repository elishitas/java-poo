
package Logica;

public class Persona {
    
    int id;
    String nombre;
    String apellido;
    String domicilio;
    int telefono;

    public Persona() {
    }

    public Persona(int id, 
            String nombre, 
            String apellido, 
            String domicilio, 
            int telefono) {
        this.id = id;
        this.nombre = nombre;
        this.apellido = apellido;
        this.domicilio = domicilio;
        this.telefono = telefono;
    }
    
    
    
}
